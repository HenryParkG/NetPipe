def add_layer(layer_type, from_layer, number, args):
    """Î†àÏù¥Ïñ¥ Ï∂îÍ∞Ä"""
    layer = {
        'from': from_layer,
        'number': number,
        'type': layer_type,
        'args': args
    }
    st.session_state.layers.append(layer)

def remove_layer(index):
    """Î†àÏù¥Ïñ¥ Ï†úÍ±∞"""
    if 0 <= index < len(st.session_state.layers):
        st.session_state.layers.pop(index)

def generate_yaml_config():
    """YAML ÏÑ§Ï†ï ÌååÏùº ÏÉùÏÑ± (YOLOv11 ÌòïÏãù)"""
    config = {
        'nc': st.session_state.model_config['nc'],
        'scales': st.session_state.model_config['scales'],
        'backbone': [],
        'head': []
    }
    
    # Î†àÏù¥Ïñ¥Î•º backboneÍ≥º headÎ°ú Î∂ÑÎ•ò
    in_head = False
    for layer in st.session_state.layers:
        layer_def = [
            layer['from'],
            layer['number'],
            layer['type'],
            layer['args']
        ]
        
        # Detect Î†àÏù¥Ïñ¥ÎÇò Ï≤´ UpsampleÏù¥ ÎÇòÌÉÄÎÇòÎ©¥ headÎ°ú Ï†ÑÌôò
        if layer['type'] in ['Detect', 'nn.Upsample'] and not in_head:
            in_head = True
        
        if in_head:
            config['head'].append(layer_def)
        else:
            config['backbone'].append(layer_def)
    
    return config

def save_model_config(filename):
    """Î™®Îç∏ ÏÑ§Ï†ï Ï†ÄÏû•"""
    config = generate_yaml_config()
    
    # YAML Ìó§Îçî Ï∂îÍ∞Ä
    yaml_header = """# Ultralytics YOLO11 Custom Model
# Custom architecture based on YOLOv11

# Parameters
"""
    
    yaml_str = yaml_header + yaml.dump(config, default_flow_style=False, sort_keys=False)
    return yaml_str

def build_pt_model(yaml_content, output_name):
    """YAML ÏÑ§Ï†ïÏùÑ Í∏∞Î∞òÏúºÎ°ú .pt Î™®Îç∏ ÌååÏùº ÏÉùÏÑ±"""
    try:
        from ultralytics import YOLO
        
        # ÏûÑÏãú YAML ÌååÏùº ÏÉùÏÑ±
        with tempfile.NamedTemporaryFile(mode='w', suffix='.yaml', delete=False) as f:
            f.write(yaml_content)
            temp_yaml_path = f.name
        
        # YAMLÎ°úÎ∂ÄÌÑ∞ Î™®Îç∏ ÏÉùÏÑ± (Í∞ÄÏ§ëÏπò Ï¥àÍ∏∞Ìôî)
        model = YOLO(temp_yaml_path)
        
        # .pt ÌååÏùºÎ°ú Ï†ÄÏû•
        temp_pt_path = temp_yaml_path.replace('.yaml', '.pt')
        model.model.save(temp_pt_path)
        
        # Ï†ÄÏû•Îêú .pt ÌååÏùº ÏùΩÍ∏∞
        with open(temp_pt_path, 'rb') as f:
            pt_data = f.read()
        
        # ÏûÑÏãú ÌååÏùº ÏÇ≠Ï†ú
        os.unlink(temp_yaml_path)
        os.unlink(temp_pt_path)
        
        return pt_data, None
        
    except ImportError:
        return None, "ultralytics Ìå®ÌÇ§ÏßÄÍ∞Ä ÏÑ§ÏπòÎêòÏñ¥ ÏûàÏßÄ ÏïäÏäµÎãàÎã§. 'pip install ultralytics' Î™ÖÎ†πÏúºÎ°ú ÏÑ§ÏπòÌï¥Ï£ºÏÑ∏Ïöî."
    except Exception as e:
        return None, f"Î™®Îç∏ ÎπåÎìú Ï§ë Ïò§Î•ò Î∞úÏÉù: {str(e)}"

